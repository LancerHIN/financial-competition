def extract_entities(text: str) -> list[str]:
    """从文本中抽取结构化主体名，仅依赖通用构词规则，不含任何数据集专有名。

    覆盖：公司全称（…有限公司/股份有限公司）、书名号引用、保险/年金/债券类产品、
    以及“…集团/公司/银行/保险/控股/证券”等通用机构后缀的短主体名。
    """
    patterns = [
        r"[\u4e00-\u9fffA-Za-z0-9]{2,30}(?:股份有限公司|集团股份有限公司|有限公司)",
        r"《[^》]{2,60}》",
        r"[\u4e00-\u9fffA-Za-z0-9]{2,40}(?:年金保险|寿险|人寿保险|公司债券|可转换公司债券)",
    ]
    values: list[str] = []
    for pattern in patterns:
        values.extend(re.findall(pattern, text))
    # 通用机构后缀短主体名（行业无关），不针对任何具体公司
    for match in re.findall(r"([\u4e00-\u9fff]{2,8})(?:集团|公司|银行|保险|控股|证券|基金)", text):
        match = re.sub(r"^(下列|以下|关于|根据|对照|结合|依据|基于|针对|有关)+", "", match)
        if match and match not in GENERIC_ENTITIES and len(match) >= 2:
            values.append(match)
    # 法规/办法/条例/指引/准则名称
    values.extend(re.findall(r"[\u4e00-\u9fff]{2,40}(?:管理办法|实施细则|条例|指引|准则|规定|通知|公告)", text))
    cleaned: list[str] = []
    for value in values:
        value = re.sub(r"^(归属于|关于|根据|对照|结合|依据|基于|公司|本|该)", "", value).strip("《》")
        if value and value not in GENERIC_ENTITIES and len(value) >= 2:
            cleaned.append(value)
    return dedupe(cleaned)[:12]


def extract_subjects(text: str) -> list[str]:
    """用于多主体覆盖判断：只保留高置信结构化主体（公司全称、书名号、产品/法规名）。

    刻意不维护任何公司简称白名单——B 榜主体未知，靠 doc_id 与年份覆盖兜底，
    短主体名不确定时只用于生成补检索 query，不作为停止条件的硬性覆盖要求。
    """
    subjects = extract_entities(text)
    # 丢弃含连接词的拼接名（如“X与Y”）和被其他主体包含的短前缀
    cleaned = [s for s in subjects if not re.search(r"[与和及、,，]", s)]
    deduped: list[str] = []
    for subject in cleaned:
        if any(subject != other and subject in other for other in cleaned):
            continue
        deduped.append(subject)
    return dedupe(deduped)[:12]


