def expand_query_terms(text: str, domain: str | None = None) -> list[str]:
    """本地 query expansion：基于别名表 + 领域同义词补充检索词，不调用任何模型。

    返回去重后的新增检索词（不含原文本身），调用方可拼到检索 query 后面。
    """
    text = str(text or "")
    financial_expansions: list[str] = []
    # 财报指标别名：双向补充（别名->标准名 与 标准名->别名）。
    # 仅在财报领域或未指定领域时启用，避免给其它领域注入财报噪声。
    if domain in (None, "", "financial_reports"):
        for alias, canonical in FINANCIAL_TERM_ALIASES.items():
            if alias in text and canonical not in text:
                financial_expansions.append(canonical)
            if canonical in text and alias not in text:
                financial_expansions.append(alias)
    domain_expansions: list[str] = []
    for trigger, extras in _DOMAIN_EXPANSION.get(domain or "", []):
        if trigger in text:
            domain_expansions.extend(extra for extra in extras if extra not in text)
    # 领域专属扩展优先于财报别名（非财报领域时财报别名为空），上限放宽到 24，
    # 避免长题干/多选题里重要扩展词被过早截断。
    return dedupe(domain_expansions + financial_expansions)[:24]


def dedupe(values: list[str]) -> list[str]:
    seen = set()
    result = []
    for value in values:
        value = str(value).strip()
        if value and value not in seen:
            seen.add(value)
            result.append(value)
    return result
